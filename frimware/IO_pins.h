// Mr. General I/O pin definitions for Diecimila, Duemilanove, Nano

#define IR_LEFT           4  // Compound Eye Left      - analog  input    A4
#define IR_RIGHT          5  // Compound Eye Right     - analog  input    A5
#define IR_UP             6  // Compound Eye Up        - analog  input    A6
#define IR_DOWN           7  // Compound Eye Down      - analog  input    A7
#define IR_LEEDS          2  // Compound Eye LEDs      - digital output   D2

#define PAN_SERVO         6  // PAN  Servo             - digital output   D5
#define TILT_SERVO        5  // TILt Servo             - digital output   D6
#define LEFT_MOTOR        9  // Left  Motor Servo      - digital output   D9
#define RIGHT_MOTOR       10 // Right Motor Servo      - digital output  D10

#define RF_SENS           3  // Front Right Sensor     - analog  input    A3
#define LF_SENS           2  // Front Left  Sensor     - analog  input    A2
#define LB_SENS           1  // Rear  Left  Sensor     - analog  input    A1
#define RB_SENS           0  // Rear  Right Sensor     - analog  input    A0

#define RF_LED            7  // Front Right  LEDs      - digital output   D7
#define LF_LED            8  // Front Left   LEDs      - digital output   D8
#define LB_LED            12 // Rear  Left   LEDs      - digital output  D12
#define RB_LED            4  // Rear  Right  LEDs      - digital output   D4

#define TX                3  // RX                      - digital output   D3
#define RX                11 // TX                     -  digital input  D11

#define SPEAKER           13 // Speaker                - digital output  D13
